
# python中实际有两种类型的列表:一种是可以改变的列表(用中括号包围),另一种一旦创建就不能改变(用小括号包围).
# 后者是一种不可变列表,更常见的称呼是元組(tuple),可以认为元組等同于列表.不过有一点区别:一旦创建,元組中的数据在任何情况下都不能改变.
# 还可以认为元組是一个"常量列表".


